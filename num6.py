#Sequence tuple
name_ab = ("Cherprang","Praewa","Areekul")

print(name_ab)

print(name_ab[2])

print(type(name_ab)) #tuple

#Sequence list
name_cd = ("Cherprang","Praewa","Areekul")

print(name_cd)

print(name_cd[2])

print(type(name_cd)) #list

#Sequence dict
name_ef = {10:"Cherprang",20:"Praewa",30:"Areekul"}

print(name_ef)

print(name_ef[20])

print(name_ef[20], name_ef[30])

print(type(name_ef)) #dict